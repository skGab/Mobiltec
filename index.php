                <p class="mb-4" data-aos="fade-right" data-aos-duration="1000">Nossa missão consiste em promover a maximização da produtividade e da lucratividade dos nossos parceiros e clientes, através do uso de dispositivos móveis.</p>

                <p class="mb-4" data-aos="fade-right" data-aos-duration="1000">Após a fundação, em março de 2003, iniciamos o desenvolvimento de um ‘mobile middleware’, para integração entre aplicações móveis e os sistemas de gestão das empresas, via redes de telefonia móvel, através de sincronização de arquivos. Este produto, o Mobiltec Mobile Middleware (M3) foi lançado em dezembro de 2004, tendo como seu primeiro cliente a então Cervejaria Schincariol. Em 2009, o M3 evoluiu para também agregar funcionalidades de MDM (Mobile Device Management), inicialmente para dispositivos Windows Mobile, e mais tarde, em 2011, também para dispositivos Android.</p>

                <figure class="d-lg-none" data-aos="zoom-in-left" data-aos-anchor-placement="top-center" data-aos-duration="1000">
                    <img src="assets/img/imagem-1-historico.png" alt="Figura Historico" class="img-fluid">
                </figure>

                <p class="mb-4" data-aos="fade-right" data-aos-duration="1000">Em 2013, fomos contemplados pela FINEP, através do edital TI Maior, com uma dotação para desenvolvimento do Cloud4mobile, uma solução de MDM oferecida ‘na nuvem’, na modalidade de serviço. O projeto FINEP iniciou em 2014, tendo sido concluído em 2016. Após a conclusão do projeto FINEP, seguimos investindo no Cloud4Mobile com recursos próprios - o que nos levou, no ano de 2017, a ser a primeira empresa da América Latina certificada pelo Google para o programa Android Enterprise.
                </p>

                <p class="mb-4" data-aos="fade-right" data-aos-duration="1000">Diretamente e através de nossos parceiros, que realizam implantação e suporte, a solução de MDM da Mobiltec atende hoje uma quantidade superior a 50 mil dispositivos em todo o Brasil, em grandes empresas como Unilever, Cervejaria Petrópolis, Rede (Banco Itaú), Café 3 Corações, entre outras.
                </p>

